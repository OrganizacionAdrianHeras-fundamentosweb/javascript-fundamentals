/*Operadores matemáticos*/
let a = 5;
let b = 2;

console.log(a + b); // 7
console.log(a - b); // 3
console.log(a * b); // 10
console.log(a / b); // 2.5
console.log(a % b); // 1

let usuario = "Carlos";

console.log("Hola " + usuario);

/*Operadores Incremento/Decremento*/
let n1 = 5;

console.log(n1); // 5
console.log(n1++); // 5
console.log(n1); // 6
console.log(++n1); // 7

console.log(n1--); // 7
console.log(n1); // 6
console.log(--n1); // 5

/*Operadores Asignación*/
let num1 = 10;

num1 = num1 + 3;
console.log(num1); // 13

num1 += 4;
console.log(num1); // 17

num1 -= 3;
console.log(num1); // 14

num1 *= 3;
console.log(num1); // 42

num1 /= 3;
console.log(num1); // 14

num1 %= 3;
console.log(num1); // 2


